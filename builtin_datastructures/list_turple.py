# list is acollection many elements separed by comma and enclosed in square brackets
list1 = [1, 2, 3, 4, 5]
print(list1)
print(type(list1))
list2 = [1, 2, 3, 4, 5, "hello", 3.14, True]
print(list2)
print(type(list2))
# lists are ordered, mutable, and allow duplicate elements
# lists are indexed, starting from 0
#  list is 0 based indexing
#list can have  negative index
# list can be nested 

list1.append(6)
print(list1)

# lists are dynamic, we can add or remove elements from the list


